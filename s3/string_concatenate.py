firstname = "John"
lastname = "Smith"

# Put the names together with "Hello" and "!":
greeting1 = "Hello "+firstname+" "+lastname+"!"

# A bit more complicated but a very flexible way:
greeting2 = "Hello {} {}!".format(firstname,lastname)

# Print all the 3 greetings:
print(greeting1)
print(greeting2)

# An empty line:
print()






















